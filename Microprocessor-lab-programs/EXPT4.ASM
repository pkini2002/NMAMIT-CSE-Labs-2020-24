;Write an ALP to reverse a given string and check whether it is a palindrome or not

DATA SEGMENT
STR1 DB 20 DUP(?)
STR2 DB 20 DUP(?)
N DB ?
MSG1 DB 10,13,"ENTER THE STRING:$"
MSG2 DB 10,13,"STRING IS A PALINDROME$"
MSG3 DB 10,13,"STRING IS NOT A PALINDROME$"
DATA ENDS

CODE SEGMENT
ASSUME CS:CODE,DS:DATA
START:MOV AX,DATA
      MOV DS,AX
      LEA SI,STR1
      LEA DI,STR2
      LEA DX,MSG1
      MOV AH,09H
      INT 21H
      CALL READSTRING
L2:   MOV N,CL
      MOV CL,00H
      DEC SI
UP1:  CMP CL,N
      JZ CHECK
      MOV AL,[SI]
      MOV [DI],AL
      DEC SI
      INC DI
      INC CL
      JMP UP1
CHECK:LEA SI,STR1
      LEA DI,STR2
      MOV CL,00H
UP2:  CMP CL,N
      JZ PAL
      MOV AL,[SI]
      CMP AL,[DI]
      JNZ NOTPAL
      INC SI
      INC DI
      INC CL
      JMP UP2
NOTPAL:LEA DX,MSG3
       MOV AH,09H
       INT 21H
       JMP EXIT
PAL:   LEA DX,MSG2
       MOV AH,09H
       INT 21H
EXIT:  MOV AH,4CH
       INT 21H

READSTRING PROC NEAR
       MOV CL,00H
UP:    MOV AH,01H
       INT 21H
       CMP AL,0DH
       JZ L1
       MOV [SI],AL
       INC SI
       INC CL
       JMP UP
L1:    RET
READSTRING ENDP
CODE ENDS
END START
