DATA SEGMENT
FILE DB 'MYFILE.TXT',0
MSG DB 10,13,"WELCOME TO MP LAB$"
BUF DB 30 DUP(?)
DATA ENDS

CODE SEGMENT
ASSUME CS:CODE,DS:DATA
START:MOV AX,DATA
      MOV DS,AX
      LEA DX,FILE
      MOV CX,0
      MOV AH,3CH ;CREATE A FILE
      INT 21H

      LEA DX,MSG
      MOV CX,30
      MOV BX,AX
      MOV AH,40H  ;WRITING IN A FILE
      INT 21H
      MOV AH,3EH  ;CLOSING A FILE
      INT 21H

      LEA DX,FILE
      MOV AL,00H
      MOV AH,3DH ;OPENING A FILE
      INT 21H

      LEA DX,BUF
      MOV BX,AX
      MOV CX,30
      MOV AH,3FH ;READING A FILE
      INT 21H
      MOV AH,3EH   ;CLOSING A FILE
      INT 21H

      LEA DX,BUF
      MOV AH,09H
      INT 21H
      MOV AH,4CH
      INT 21H
CODE ENDS
END START
