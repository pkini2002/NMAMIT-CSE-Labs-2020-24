DATA SEGMENT
N DB 06H
FACT DW ?
MSG DB 10,13,"THE FACTORIAL OF 6 IS:$"
DATA ENDS

CODE SEGMENT
ASSUME CS:CODE,DS:DATA
START:MOV AX,DATA
      MOV DS,AX
      MOV AX,1
      MOV BL,N
      MOV BH,00
      CALL FACTORIAL
      MOV FACT,AX
      MOV BL,AH
      LEA DX,MSG
      MOV AH,09H
      INT 21H
      CALL DISPHEXA
      MOV BX,FACT
      CALL DISPHEXA
      MOV AH,4CH
      INT 21H

FACTORIAL PROC NEAR
      CMP BX,1
      JE L1
      PUSH BX
      DEC BX
      CALL FACTORIAL
      POP BX
      MUL BX
L1:   RET
FACTORIAL ENDP

DISPHEXA PROC NEAR 
     MOV DL,BL
     MOV CL,04
     SHR DL,CL
     CMP DL,09H
     JBE L2
     ADD DL,07H
L2:  ADD DL,30H
     MOV AH,02H
     INT 21H
     MOV DL,BL
     AND DL,0FH
     CMP DL,09H
     JBE L3
     ADD DL,07H
L3:  ADD DL,30H
     MOV AH,02H
     INT 21H
     RET
DISPHEXA ENDP

CODE ENDS
END START
